def translate(value: str, request: Request) -> str:
    language = request.cookies.get('checklectura-language', 'es')
    return TRANSLATIONS.get(language, {}).get(value, value)


def translate_month_label(value: str, request: Request) -> str:
    language = request.cookies.get('checklectura-language', 'es')
    parts = value.split(' ', maxsplit=1)
    month = MONTH_TRANSLATIONS.get(language, {}).get(parts[0].lower(), parts[0])
    return f'{month.capitalize()} {parts[1]}' if len(parts) == 2 else month.capitalize()
